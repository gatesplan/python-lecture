"""
p190 분기 한정법으로 자연수의 합으로 n을 나타낼 수 있는 모든 순열의 개수 구하기
(10 이하의 자연수 n이 주어질 때)

예제 (n=4):
- 1+1+1+1 = 4
- 1+1+2 = 4
- 1+2+1 = 4
- 2+1+1 = 4
- 2+2 = 4
- 1+3 = 4
- 3+1 = 4
- 4 = 4
총 8가지

분기 한정법(Branch and Bound):
- 불필요한 경로는 조기 종료 (가지치기)
- s > n이면 더 이상 탐색할 필요 없음
"""

ans = 0

def f(s, k):
    """
    재귀 함수로 합의 순열 개수 계산

    Parameters:
    - s: 지금까지의 합
    - k: 더 더할 수 있는 남은 숫자 개수

    Returns:
    - 조건을 만족하는 경우의 수
    """
    # 종료 조건1: 더 더할 수 있는 숫자 개수가 0일 때
    # (더 깊이 더하면 안 되고, 조건 만족하는지 확인해서 종료해야 함)
    if k == 0:
        if n == s:  # 지금까지의 합이 n과 같으면
            return 1  # 밖에서 1개 값으로 쓸 1 반환
        return 0  # 밖에서 더할 때 0을 더하게 되도록 조건 만족 안 하면 0 반환

    # 종료 조건2: 합이 n을 초과하면 더 해봐야 의미가 없으므로 중단 (가지치기)
    if s > n:
        return 0

    # 이제부터 더 더할 수 있는 숫자 개수가 남아 있고,
    # 지금까지의 합이 n을 초과하지 않았으므로 더 더해 봄
    cnt = 0
    # cnt = 0은 재귀호출 트리에서 이 시점에서부터 하위 트리의 개수를 누적해서 저장할 변수.
    # 이것이 return되어 상위 호출에서 더해짐.
    for i in range(1, n+1):
        # 추가로 더할 수 i. i는 1부터 n까지 모두 시도.
        cnt = cnt + f(s + i, k - 1)

    return cnt

n = int(input())

# 1개로 만드는 경우, 2개로 만드는 경우, ..., n개로 만드는 경우 모두 시도
for i in range(1, n+1):
    ans = ans + f(0, i)

print(ans)

# 시간복잡도: O(n^n) (최악의 경우, 가지치기로 실제로는 더 적음)

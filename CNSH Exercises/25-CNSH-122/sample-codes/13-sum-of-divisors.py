"""
약수의 합 구하기

문제:
- 자연수 n이 주어질 때, n의 모든 약수의 합 구하기

약수(Divisor):
- n을 나누어떨어지게 하는 수
- 예: 12의 약수는 1, 2, 3, 4, 6, 12

알고리즘:
- 1부터 n까지 모든 수를 확인
- n을 나누어떨어지게 하는 수를 찾아 합산

시간복잡도: O(n)

최적화 가능:
- √n까지만 확인하고 쌍을 찾으면 O(√n)으로 개선 가능
- 예: 12의 약수를 찾을 때
  - i=1: 1과 12/1=12 (1 + 12)
  - i=2: 2와 12/2=6 (2 + 6)
  - i=3: 3과 12/3=4 (3 + 4)
  - √12 ≈ 3.46이므로 여기까지만 확인
"""

ans = 0
n = 1000

# 직접 구하기 방법
for i in range(1, n+1):
    if n % i == 0:
        ans = ans + i

print(ans)

# n = 1000일 때 약수: 1, 2, 4, 5, 8, 10, 20, 25, 40, 50, 100, 125, 200, 250, 500, 1000
# 합 = 2340


# 최적화 버전 (O(√n))
def sum_of_divisors_optimized(n):
    """
    약수의 합을 O(√n)에 구하는 최적화 버전
    """
    result = 0
    i = 1
    while i * i <= n:
        if n % i == 0:
            result += i  # i는 약수
            if i != n // i:  # i와 n//i가 다르면 (제곱수가 아니면)
                result += n // i  # n//i도 약수
        i += 1
    return result

print(f"최적화 버전: {sum_of_divisors_optimized(n)}")

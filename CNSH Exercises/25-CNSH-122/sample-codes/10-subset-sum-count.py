"""
p191 스스로 해 보기
10 이하의 서로 다른 n개의 자연수가 주어질 때,
이 자연수들의 합으로 s를 만들 수 있는 모든 경우의 수 구하기

입력:
- n: 자연수 개수
- s: 목표 합
- A: n개의 서로 다른 자연수 배열

출력:
- A의 원소들로 s를 만들 수 있는 경우의 수

예제:
n = 4, s = 10, A = [3, 5, 2, 8]
- 3 + 5 + 2 = 10 (O)
- 2 + 8 = 10 (O)
총 2가지 (중복 선택 가능한 경우)
"""

def f(c, k):
    """
    재귀 함수로 부분집합 합 경우의 수 계산

    Parameters:
    - c: 지금까지의 합
    - k: 더 더할 수 있는 남은 숫자 개수

    Returns:
    - 조건을 만족하는 경우의 수
    """
    # 종료 조건1: 더 더할 수 있는 숫자 개수가 0일 때
    if k == 0:
        if c == s:
            return 1
        return 0

    # 종료 조건2: 지금까지의 합이 s를 초과하면 중단 (가지치기)
    if c > s:
        return 0

    # 이제부터 더 더할 수 있는 숫자 개수가 남아 있고,
    # 지금까지의 합이 s를 초과하지 않았으므로 더 더해 봄
    cnt = 0
    for i in range(n):
        # A[i]를 더함 (중복 선택 가능)
        cnt = cnt + f(c + A[i], k - 1)
    return cnt

# 입력
n = int(input())
s = int(input())
A = list(map(int, input().split()))

# 1개로 만드는 경우, 2개로 만드는 경우, ..., n개로 만드는 경우
ans = 0
for i in range(n):
    ans = ans + f(0, i)

print(ans)

# 시간복잡도: O(n^n) (최악의 경우)

"""
동전 최소 개수 구하기 - 탐욕법(Greedy Algorithm)

탐욕법:
- 매 순간 최선의 선택을 하는 알고리즘
- 큰 동전부터 최대한 많이 사용

장점:
- 시간복잡도: O(k) (k: 동전 종류 개수)
- 매우 빠름

주의사항:
- 동전의 단위가 배수 관계일 때만 최적해 보장
- 예: [500, 100, 50, 10]은 OK
- 예: [500, 400, 100]은 최적해 보장 안 됨
  (800원: 탐욕법은 500+100+100+100=4개, 최적해는 400+400=2개)

시간복잡도: O(k) where k = len(coin)
"""

ans = 0
coin = [500, 100, 50, 10]
n = 1780

# 큰 동전부터 최대한 많이 사용
for c in coin:
    ans = ans + n // c  # 해당 동전으로 나눈 몫을 더하면 그게 그 동전 사용 개수
    n = n % c  # 나머지 금액을 다음 동전으로 처리하기 위해 저장

print(ans)

# 계산 과정:
# 1780 ÷ 500 = 3개, 나머지 280
#  280 ÷ 100 = 2개, 나머지 80
#   80 ÷ 50  = 1개, 나머지 30
#   30 ÷ 10  = 3개, 나머지 0
# 총 3 + 2 + 1 + 3 = 9개

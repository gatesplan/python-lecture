"""
p181 소수 구하기 예제 (기본 버전)
시간복잡도: O(n^2)

복잡도 클래스:
- O(1): 상수 시간
- O(log n): 로그 시간
- O(n): 선형 시간
- O(n log n): 선형로그 시간
- O(n^2): 이차 시간
- O(2^n): 지수 시간
"""

ans = 0
n = 100

for i in range(2, n+1):  # n번 반복
    cnt = 0
    # i가 소수인지 확인
    # 예: i = 7
    for j in range(2, i):  # i번 반복 (최악의 경우)
        if (i % j) == 0:
            cnt = 1

    # cnt가 0이면 약수가 없으므로 소수
    if cnt == 0:
        ans = ans + 1

print(ans)

# 시간복잡도 분석:
# 외부 루프: n번
# 내부 루프: 평균 n/2번
# 전체: O(n^2)

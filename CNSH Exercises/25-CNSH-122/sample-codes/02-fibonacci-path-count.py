"""
p176 스스로해보기2
피보나치 문제 - 0에서 출발하여 1 또는 2 더하여 n 도달하는 경우의 수

문제 해석:
- 피보나치 역산으로 생각하면 n에서 1 또는 2를 빼가며 0에 도달하는 것으로 바꿀 수 있다
- 재귀 트리 구조:
  n
    n-1
        n-2
        n-3
    n-2
        n-3
        n-4
"""

def f(n):
    if n == 2:
        return 2
    if n == 1:
        return 1
    return f(n-1) + f(n-2)

# 테스트: f(4) = 5
print(f(4))
